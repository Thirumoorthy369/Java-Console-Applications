import static java.lang.Math.*;
public class Caci1 {

        static void add ( int a, double b){
            System.out.println(a + b);
        }
        static void add ( int a, double b, byte c){
            System.out.println(a + b + c);
        }
        static void sub ( int a, double b){
            System.out.println(a - b);
        }
        static void sub ( int a, double b, byte c){
            System.out.println(a - b - c);
        }
        static void mul ( int a, double b){
            System.out.println(a * b);
        }
        static void mul ( int a, double b, byte c){
            System.out.println(a * b * c);
        }
        static void div ( int a, double b){
            System.out.println(a / b);
        }
        static void div ( int a, double b, byte c){
            System.out.println(a / b / c);
        }
        static void exp ( int a, double b){
            System.out.println(Math.pow(a, b));
        }
    }










